// graphics-renderer-editor.js
// Graphics renderer editing and saving

// Initialize graphics renderer editor variables
let graphicsRendererEditor;

// Initialize graphics renderer editor
function initGraphicsRendererEditor() {
  graphicsRendererEditor = new GraphicsRendererEditor();
}

// Edit graphics renderer
function editGraphicsRenderer() {
  // Edit graphics renderer
  graphicsRendererEditor.edit();
}

// Save graphics renderer
function saveGraphicsRenderer() {
  // Save graphics renderer
  graphicsRendererEditor.save();
}
