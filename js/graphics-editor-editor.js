// graphics-editor-editor.js
// Graphics editor editing and saving

// Initialize graphics editor editor variables
let graphicsEditorEditor;

// Initialize graphics editor editor
function initGraphicsEditorEditor() {
  graphicsEditorEditor = new GraphicsEditorEditor();
}

// Edit graphics editor
function editGraphicsEditor() {
  // Edit graphics editor
  graphicsEditorEditor.edit();
}

// Save graphics editor
function saveGraphicsEditor() {
  // Save graphics editor
  graphicsEditorEditor.save();
}
